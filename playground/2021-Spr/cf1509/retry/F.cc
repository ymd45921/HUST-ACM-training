/**
 *
 * 首先，肯定是尽可能用 0 边，然后把值全部给一条不太会用到的边
 *
 * 首先，我们先尝试用 0 边 DFS，找出所有可以联通的连通块
 * 如果这个过程没有使用完所有的 0 边，我们就可以把异或值加到空边上
 * 接下来，在已经跑过空边的图上运行 MST 算法，并且：
 *   如果有两块还没有被连接起来，那么可以使用这条最小边连接两块
 *   否则，因为异或值一定会被赋值到一条空边上，尝试两害相权取其轻
 *     如果两块不是全部依靠实边连接的，那么它们一定依靠了虚边
 *     如果这条边的权重更低，那么考虑用它来置换出一条虚边
 *     因为虚边的权值（异或和）是单例维护的，所以只会加一次
 *   综上所述，我们得到了一种求解这个题目的办法
 * 但是这个在虚边图上 DFS 的做法复杂度是真的玄学，，不知道怎么能过
 * 算了，分析锤子的复杂度，，放弃思考了！（
 * 
 * 学会了一种更加优雅的按秩合并的并查集的写法，命名为 ya_dsu
 * 负数表示是根节点，返回根节点 id；绝对值存储连通块的大小
 */
