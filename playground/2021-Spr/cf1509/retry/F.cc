class ya_dsu {
    vector<int> fa;

    void join(int u, int v) {
        if (fa[u] > fa[v]) swap(u, v);
        fa[u] += fa[v], fa[v] = u;
    }

public:

    explicit ya_dsu(int n) : fa(n, -1) {}

    void clear() { fill(fa.begin(), fa.end(), -1); }

    int id(int u) { return fa[u] < 0 ? u : fa[u] = id(fa[u]); }

    bool connect(int u, int v) {
        u = id(u), v = id(v);
        if (u == v) return false;
        else return join(u, v), true;
    }

    int size(int u) { return -fa[id(u)]; }
};

const int N = 2e5 + 5;

set<int> disc, adj[N];

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(null), cout.tie(null);

    int n, m, u, v, w;
    input, n, m;
    auto res = (longs) n * (n - 1) / 2 - m;
    for (int i = 1; i <= n; ++i) disc.insert(i);
    ya_dsu all(n + 1), real(n + 1);
    vector<tuple<int, int, int>> edge;
    const function<void(int)> dfs = [&](int u) {
        disc.erase(u);
        for (int v = 0;;) {
            auto it = disc.lower_bound(v);
            if (it == disc.end()) return;
            else v = *it;
            if (!adj[u].count(v)) {
                all.connect(u, v);
                dfs(v), --res;
            }
            ++v;
        }
    };
    uint xs = 0;
    while (m--) {
        input, u, v, w;
        adj[u].insert(v), adj[v].insert(u);
        edge.emplace_back(w, u, v);
        xs ^= (uint) w;
    }
    for (int i = 1; i <= n; ++i)
        if (disc.count(i)) dfs(i);
    if (res > 0) xs = 0;
    sort(edge.begin(), edge.end());
    longs ans = 0;
    for (auto[w, u, v] : edge) {
        if (all.connect(u, v)) {
            ans += w;
            real.connect(u, v);
        } else if (real.connect(u, v))
            minimize(xs, (uint)w);
    }
    output, ans + xs, endl;
    return 0;
}