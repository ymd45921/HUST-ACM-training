static class Scanner
{
    template<class T>
    inline T read()
    {
        T x = 0; int f = 0, ch = getchar();
        while (!isdigit(ch)) ch == '-' && (f = !f), ch = getchar();
        while (isdigit(ch)) x = x * 10 + ch - 48, ch = getchar();
        return f ? -x : x;
    }

    static inline bool isBlank(int x)
    {return x == ' ' || x == '\n';}
public:

    template <class T>
    void operator()(T &x){x = read<T>();}

    template <class T, class... Ts>
    void operator()(T &x, Ts &... y)
    {(*this)(x), (*this)(y...);}
    template <class T, class... Ts>
    void operator()(T &&x, Ts &&... y)
    {(*this)(x), (*this)(y...);}

    void operator()(char *x){scanf("%s", x);}
    void operator()(char &x){do x = (char)getchar(); while (isBlank(x));}

    int nextInt() {return read<int>();}
    longs nextLongs() {return read<longs>();}
    lll nextInt128() {return read<lll>();}
    char nextChar() {char x; (*this)(x); return x;}
} scanner;

void solve(vector<int> &ans, int n, int k)
{
    if (k == 1) ans.push_back(n);
    else if (k * 2 >= n)
    {
        int cnt2 = n - k, cnt1 = k - cnt2;
        while (cnt2 --) ans.push_back(2);
        while (cnt1 --) ans.push_back(1);
    }
    else if (__builtin_popcount(n) == 1)
        ans.push_back(n / 2), solve(ans, n / 2, k - 1);
    else if (!(n % k))
    {
        int value = n / k;
        while (k --) ans.push_back(value);
    }
    else if (n % 2)
    {
        ans.push_back(1);
        solve(ans, n - 1, k - 1);
    }
    else
    {
        vector<int> tmp;
        int mod2 = 1;
        while (!(n % 2) && n >= 2 * k)
            n /= 2, mod2 *= 2;
        solve(tmp, n, k);
        for (auto ii : tmp) ans.push_back(ii * mod2);
    }
}

