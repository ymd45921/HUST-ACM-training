template<class T>
void gcd_assign(T &a, T b) { a = gcd(a, b); }

template<class T>
class fws {

    struct edge {
        int u{}, v{};
        T w;
        long next{};

        edge() = default;

        edge(int u, int v, T w, int x) : u(u), v(v), w(w), next(x) {}

        auto operator*() const { return make_tuple(u, v, ref(w)); }
    };

    vector<int> head;
    vector<edge> e;

public:
    using edge_view = decltype(*edge());

    using iterator = typename vector<edge>::iterator;

    explicit fws(size_t siz) : head(siz, -1) {}

    fws(size_t siz, size_t es) : head(siz, -1) { e.reserve(es); }

    void clear() { e.clear(), fill(head.begin(), head.end(), -1); }

    void reserve(size_t es) { e.reserve(es); }

    void add_edge(int u, int v, T w = 1) {
        edge tmp(u, v, w, head[u]);
        head[u] = e.size();
        e.emplace_back(std::move(tmp));
    }

    void for_each(int u, function<bool(int, edge_view)> fun) {
        for (int cur = head[u]; cur != -1; cur = e[cur].next)
            if (!fun(cur, *e[cur])) break; else continue;
    }

    iterator begin() { return e.begin(); }

    iterator end() { return e.end(); }
};

int scc[N], ncc = 0;
longs gcc[N], d[N];
bitset<N> vis[2];

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(null), cout.tie(null);

    int n, m, u, v, w, q, s, t;
    $(n, m);
    fws<int> g(n + 1, m + 5), r(n + 1, m + 5);
    using edge = decltype(g)::edge_view;
    while (m --) {
        $(u, v, w);
        g.add_edge(u, v, w);
        r.add_edge(v, u, w);
    }
    vector<int> order;
    const function<void(int)> dfs0 = [&](int u) {
        vis[0].set(u);
        g.for_each(u, [&](int id, edge e) {
            auto [u, v, w] = e;
            if (!vis[0].test(v)) dfs0(v);
            return true;
        });
        order.push_back(u);
    };
    const function<void(int)> dfs1 = [&](int u) {
        vis[1].set(u);
        r.for_each(u, [&](int id, edge e) {
            auto [u, v, w] = e;
            if (!vis[1].test(v)) {
                scc[v] = scc[u];
                d[v] = d[u] + w;
                dfs1(v);
            } else if (scc[v] == scc[u])
                gcd_assign(gcc[scc[u]], abs(d[u] + w - d[v]));
            return true;
        });
    };
    order.reserve(n + 5);
    for (int i = 1; i <= n; ++ i)
        if (!vis[0][i]) dfs0(i);
    reverse(order.begin(), order.end());
    for (auto ii : order)
        if (!vis[1][ii]) {
            scc[ii] = ++ ncc, d[ii] = 0;
            dfs1(ii);
        }
    for ($(q); q --;) {
        $(v, s, t);
        $.put(s % gcd(gcc[scc[v]], t) ? "NO" : "YES");
    }

    return 0;
}